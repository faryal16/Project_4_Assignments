def main():
    fruit_list = ['apple', 'banana', 'orange', 'grape', 'pineapple']
    
    print(f"\nActual List:", fruit_list)
    print(f"\nThe length of Fruite list is: {len(fruit_list)}")
    
    fruit_list.append('mango')
    
    print(f"\nUpdated fruit list:", fruit_list)
    print(f"\nThe length of Fruite list after updated is: {len(fruit_list)}")
    
    
    
    
if __name__ == "__main__":
    main()    